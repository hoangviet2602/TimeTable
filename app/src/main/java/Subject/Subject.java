package Subject;

public class Subject {
    int id;
    String tittle;
    String hinhanh;

    public Subject(int id, String tittle, String hinhanh) {
        this.id = id;
        this.tittle = tittle;
        this.hinhanh = hinhanh;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTittle() {
        return tittle;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public String getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(String hinhanh) {
        this.hinhanh = hinhanh;
    }
}
